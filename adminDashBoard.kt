 Admin Panel – Flutter Web UI Structure
✅ 1. Page/Screen Structure
Route	Screen Name	Description
/dashboard	DashboardPage	Total bookings, vendors, revenue, charts
/vendors	VendorListPage	List + approve/reject + view profile
/users	UserListPage	View & suspend users
/bookings	BookingListPage	Filter, search, manage
/reviews	ReviewModerationPage	View/delete reviews
/categories	CategoryManagerPage	Add/Edit/Delete categories
/payments	RevenuePage	Total income, charts, transactions
/notifications	NotificationPage	Send push / announcements
/settings	SettingsPage	Admin settings, theme, etc.